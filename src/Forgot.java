    public void search(){
    
        String a1 = txtUsername.getText();
        String sql="select * from Account where Username='"+a1+"'";
        
        try{
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){   //if username correct
                txtName.setText(rs.getString(2));
                txtSecurityQuestion.setText(rs.getString(4));
                rs.close();
                pst.close();
            }else{
                JOptionPane.showMessageDialog(null,"Incorrect Username");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
    }
    
    public void retrieve(){
    
        String a1 = txtUsername.getText();
        String a2 = txtAnswer.getText();
        String sql = "select * from Account where Answer= '"+a2+"'";
        
        try{
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            
            if(rs.next()){
                txtYourPassword.setText(rs.getString(3));
            }
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
    }
    
    
